#conditional statements არის პროგრამირების ძირითადი კონცეფცია, რომელიც გამოიყენება გადაწყვეტილებების მისაღებად
#if არის პროგრამირების კონსტრუქცია, რომელიც საშუალებას გაძლევს, შეამოწმო პირობა.
#elif არის კონსტრუქცია, რომელიც გამოიყენება if პირობის ალტერნატიული გზების შესამოწმებლად
#else არის  პირობითი კონსტრუქცია, რომელიც გამოიყენება იმ კოდის შესასრულებლად, როცა if და elif პირობები არ შესრულდება